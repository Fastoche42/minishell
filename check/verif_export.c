int	verif_export(char *cmd)
{
	if (cmd[0] == 'e' && cmd[1] == 'x' && cmd[2] == 'p' && cmd[3] == 'o'
		&& cmd[4] == 'r' && cmd[5] == 't' && cmd[6] == '\0')
		return (0);
	return (1);
}
