#include "../includes/minishell.h"

int	verif_exit(char *cmd)
{
	if (cmd[0] == 'e' && cmd[1] == 'x' && cmd[2] == 'i'
		&& cmd[3] == 't' && cmd[4] == '\0')
		return (0);
	return (1);
}
