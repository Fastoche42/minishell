#include "../includes/minishell.h"

int	exec_cd(t_cmdlist *cmd)
{
	
	if (chdir(cmd->cmd_arg[1]) == 0)
		return (0);
	else
		return (error_manager(9), ft_putendl_fd(strerror(errno), 2, 1));
}
