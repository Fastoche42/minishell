#include "libft.h"

int	ft_putendl_fd(char *s, int fd, int ret)
{
	if (s)
	{
		while (*s != '\0')
		{
			ft_putchar_fd(*s, fd);
			s++;
		}
		ft_putchar_fd('\n', fd);
	}
	return (ret);
}
