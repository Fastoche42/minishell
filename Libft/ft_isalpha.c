#include "libft.h"

int	ft_isalpha(int c)
{
	int	x;

	x = 0;
	if ((c >= 'a' && c <= 'z')
		|| (c >= 'A' && c <= 'Z'))
	{	
		x = 1;
	}	
	return (x);
}
