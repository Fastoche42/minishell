#include "libft.h"

int	ft_putstr_fd(char *str, int fd, int ret)
{
	if (!(!str))
	{
		while (*str != '\0')
		{
			ft_putchar_fd(*str, fd);
			str++;
		}
	}
	return (ret);
}
